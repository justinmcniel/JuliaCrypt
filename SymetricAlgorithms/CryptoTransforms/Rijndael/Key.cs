            get => ExpandedKey.AsSpan(round * Nb / 2, Nb / 2);
        }

        private unsafe void Expand()
        {
            fixed (UInt64* ptr = ExpandedKey)
            {
                var W = (UInt32*)ptr;
                UInt32 tmp;
                bool bigKey = Nk > 6;
                for (int i = Nk; i < WordCount; i++)
                {
                    tmp = W[i - 1];
                    if (i % Nk == 0)
                    { tmp = Utilities.SubByte(Utilities.RotByte(tmp)) ^ (Rcon[i / Nk] << 24); }
                    else if (i % Nk == 4 && bigKey)
                    { tmp = Utilities.SubByte(tmp); }
                    W[i] = W[i - Nk] ^ tmp;
                }
            }
        }

