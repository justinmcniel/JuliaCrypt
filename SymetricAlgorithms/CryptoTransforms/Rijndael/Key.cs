        private UInt64[] ExpandedKey;
        public readonly int Nk;
        private readonly int Nb;
        private readonly int WordCount;
        public Key(byte[] CipherKey, int Nb, int Nr, int Nk)
        {
            this.Nk =Nk;
            this.Nb = Nb;
            WordCount = Nb * (Nr + 1);
            ExpandedKey = new UInt64[WordCount / 2];
            Buffer.BlockCopy(CipherKey, 0, ExpandedKey, 0, CipherKey.Length);
            Expand();
        }

        public Span<UInt64> this[int round]
